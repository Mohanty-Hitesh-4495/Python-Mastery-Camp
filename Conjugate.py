# Define complex numbers a and b
a = -5 + 5j
b = 8 - 3j

# Find the conjugates of complex numbers a and b
cona = a.conjugate()
conb = b.conjugate()

# Print the original complex numbers and their conjugates
print("Original complex number a:", a)
print("Conjugate of a:", cona)
print("Conjugate of b:", conb)

# Separate real and imaginary parts of complex number a
real = a.real
img = a.imag

# Print real and imaginary parts of a
print("Real part of a:", real)
print("Imaginary part of a:", img)
